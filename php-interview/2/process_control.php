/**
 * PHP的遍历数组的三种方式及各自区别
 * 三种方式：
 * for
 * foreach
 * while、list()、each()组合循环
 * 区别：
 * for 循环只能遍历索引数组，foreach 可以遍历索引和关联数组，联合使用list()、each()和while 循环同样可以索引和关联数组
 * list()、each()和while 不会 reset 数组
 * foreach 会 reset 数组
 */

/**
 * if...elseif
 * 在 elseif 语句中只能有一个表达式为 true，即在 elseif 语句中只能有一个语句块被执行，
 * 多个 elseif 从句是排斥关系。
 * 使用 elseif 语句有一个基本原则，总把优先范围小的条件放在前面处理。
 */